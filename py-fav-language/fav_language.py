#This program takes your favorite programming language as input and outputs a reaction based on the language you enter.

print("What is your favorite programming language?")
fav_lang = input()

if fav_lang == "Python":
	print("Noob!")
elif fav_lang == "Java":
	print("Weirdo!")
elif fav_lang == "C":
	print("Boomer!")
elif fav_lang == "C++":
	print("Wow, you're a chad")
elif fav_lang == "JavaScript":
	print("You and everyone else...")
else:
	print("I don't really know that one...")
