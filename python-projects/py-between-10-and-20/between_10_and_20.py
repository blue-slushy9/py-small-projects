#This program takes an integer or float as input and outputs whether the number is between 10 and 20.

print("This program takes an integer or float as input and outputs whether the number is between 10 and 20. Please enter your number now...")
num = float(input())

if num >= 10 and num <= 20:
    print("This number is between 10 and 20")
else:
    print("This number is not between 10 and 20")

