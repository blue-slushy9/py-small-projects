#Create two lists, one with first names and one with corresponding last names 

first_names = ["Jerry", "Harley", "Emilio", "Peter"]
last_names = ["Springer", "Quinn", "Estevez", "Parker"]

#Print out everyone's full name

for i in range(len(first_names)):
    print(f"{first_names[i]} {last_names[i]}")

